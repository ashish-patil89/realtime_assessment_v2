import 'package:flutter/material.dart';

/// The font weights for the App UI Kit.
abstract class AppFontWeights {
  /// The font weight value of `w900`.
  static const FontWeight black = FontWeight.w900;

  /// The font weight value of `w800`.
  static const FontWeight extraBold = FontWeight.w800;

  /// The font weight value of `w700`.
  static const FontWeight bold = FontWeight.w700;

  /// The font weight value of `w600`.
  static const FontWeight semiBold = FontWeight.w600;

  /// The font weight value of `w500`.
  static const FontWeight medium = FontWeight.w500;

  /// The font weight value of `w400`.
  static const FontWeight regular = FontWeight.w400;

  /// The font weight value of `w300`.
  static const FontWeight light = FontWeight.w300;

  /// The font weight value of `w200`.
  static const FontWeight extraLight = FontWeight.w200;

  /// The font weight value of `w100`.
  static const FontWeight thin = FontWeight.w100;
}
